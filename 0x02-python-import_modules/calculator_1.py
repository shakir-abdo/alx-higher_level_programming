#!/usr/bin/python3

def add(a, b):
    """addition function

    args:
        a: the first integer
        b: the second integer

    returns:
        return value. a + b
    """
    return (a + b)


def sub(a, b):
    """subtraction function"""
    return (a - b)


def mul(a, b):
    """multiplication function"""
    return (a * b)


def div(a, b):
    """division function"""
    return int(a / b)
